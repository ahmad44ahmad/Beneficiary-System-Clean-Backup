-- Create a view for dashboard statistics
CREATE OR REPLACE VIEW accountability_stats AS
SELECT COUNT(*) as total_issues,
    COUNT(*) FILTER (
        WHERE severity = 'critical'
    ) as critical_count,
    COUNT(*) FILTER (
        WHERE severity = 'high'
    ) as high_count,
    COUNT(*) FILTER (
        WHERE severity = 'medium'
    ) as medium_count,
    COUNT(*) FILTER (
        WHERE severity = 'low'
    ) as low_count,
    COUNT(*) FILTER (
        WHERE days_pending > 730
    ) as over_2_years,
    COUNT(*) FILTER (
        WHERE days_pending > 365
    ) as over_1_year,
    COUNT(*) FILTER (
        WHERE evasion_type = 'forward_escape'
    ) as forward_escape_count,
    COUNT(*) FILTER (
        WHERE evasion_type = 'misdirection'
    ) as misdirection_count,
    COUNT(*) FILTER (
        WHERE evasion_type = 'false_promise'
    ) as false_promise_count,
    COUNT(*) FILTER (
        WHERE evasion_type = 'silence'
    ) as silence_count,
    ROUND(AVG(days_pending)) as avg_pending_days,
    COUNT(*) FILTER (
        WHERE requires_attention = true
            AND acknowledged = false
    ) as needs_attention
FROM accountability_gaps;